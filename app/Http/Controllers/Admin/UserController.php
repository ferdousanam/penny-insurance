    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    protected $role_id;
    public function __construct() {
        $this->middleware(function ($request, $next) {
            $this->role_id = Auth::user()->role_id;
            if ($this->role_id != 1 && $this->role_id != 4) {
                return abort(404);
            }
            return $next($request);
        });
    }

